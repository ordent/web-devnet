function User() {
  function getId(){
    return this.id;
  }

  function getUsername(){
    return this.username;
  }

  function getRank(){
    return this.rank;
  }

  function getSkills(){
    return this.skill;
  }

  function getBio(){
    return this.bio;
  }

  function getProjects(){
    return this.projects;
  }

  function getJobs(){
    return this.jobs;
  }

  function getActiveJobs(){
    return this.activeJobs;
  }

  function getScore(){
    return this.score;
  }

  function getGender(){
    return this.gender;
  }

  function getEmail(){
    return this.email;
  }

  function getPhone(){
    return this.phone;
  }

}